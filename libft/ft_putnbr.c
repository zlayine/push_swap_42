#include "libft.h"

void	ft_putnbr(long long d)
{
	if (d < 0)
	{
		ft_putchar('-');
		d = d * -1;
	}
	if (d / 10)
		ft_putnbr(d / 10);
	ft_putchar(d % 10 + 48);
}
