char	*ft_strchr(const char *s, int c)
{
	char	r;
	int		i;

	r = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == r)
			return ((char *)(s + i));
		i++;
	}
	if (r == '\0')
		return ((char *)s + i);
	return (0);
}
